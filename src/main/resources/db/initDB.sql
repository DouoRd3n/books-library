CREATE TABLE person
(
    id       INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    login    VARCHAR(255),
    password VARCHAR(255),
    role     INTEGER,
    CONSTRAINT pk_person PRIMARY KEY (id)
);
CREATE TABLE books
(
    id     INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    title  VARCHAR(255),
    author VARCHAR(255),
    CONSTRAINT pk_books PRIMARY KEY (id)
);